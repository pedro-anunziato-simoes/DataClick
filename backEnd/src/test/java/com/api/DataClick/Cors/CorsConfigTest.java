@ExtendWith(MockitoExtension.class)
public class CorsConfigTest {

    @Mock
    private SecurityFilter securityFilter;

    @InjectMocks
    private CorsConfig corsConfig;


    @Test
    void configure_DeveConfigurarChainCorretamente() throws Exception {
        HttpSecurity http = mock(HttpSecurity.class);

        when(http.cors(any())).thenReturn(http);
        when(http.csrf(any())).thenReturn(http);
        when(http.sessionManagement(any())).thenReturn(http);
        when(http.authorizeHttpRequests(any())).thenReturn(http);
        when(http.addFilterBefore(any(), any())).thenReturn(http);

        corsConfig.configure(http);

        verify(http).csrf(any());
        verify(http).sessionManagement(any());
        verify(http).addFilterBefore(eq(securityFilter), eq(UsernamePasswordAuthenticationFilter.class));
    }

    @Test
    void passwordEncoder_DeveRetornarBCryptEncoder() {
        PasswordEncoder encoder = corsConfig.passwordEncoder();
        assertTrue(encoder instanceof BCryptPasswordEncoder);
    }

}
